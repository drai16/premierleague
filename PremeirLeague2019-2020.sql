--create a TABLES 
CREATE TABLE stadium(
	stadium_id INT NOT NULL, 
	stadium_name varchar(45), 
	stadium_opened int, 
	stadium_location varchar(45), 
	stadium_capacity INT, 

	CONSTRAINT pk_stadium Primary key(stadium_id)

)

Go 

CREATE TABLE referee(
	referee_id INT Not null, 
	referee_name varchar(50), 
	
	PRIMARY KEY(referee_id)
)

GO

CREATE TABLE player(
	player_id INT NOT NULL, 
	player_name varchar(50), 
	player_position char(10), 

	PRIMARY KEY(player_id) 
)

GO

CREATE TABLE team(
	team_id INT NOT NULL, 
	team_name varchar(45), 
	team_manager varchar(50), 
	stadium_id INT not null, 

	PRIMARY KEY(team_id), 
	CONSTRAINT fk_team_stadium FOREIGN KEY(stadium_id) REFERENCES  stadium(stadium_id) 
)

GO 

CREATE TABLE matches(
	match_id INT not null,
	match_week INT not null, 
	match_date date not null,	
	Home_team int not null, 
	Away_team int not null,
	stadium_id int not null,
	referee_id int not null, 

	constraint matches_pk PRIMARY KEY(match_id),

	constraint fk_home_team_team FOREIGN KEY(Home_team) REFERENCES team(team_id), 
	constraint fk_away_team_team FOREIGN KEY(Away_team) REFERENCES team(team_id), 
	constraint fk_matches_stadium FOREIGN KEY(stadium_id) REFERENCES stadium(stadium_id), 
	constraint fk_matches_referee FOREIGN KEY(referee_id) REFERENCES referee(referee_id) 

)

GO 

